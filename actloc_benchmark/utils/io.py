def read_points3D_binary(path_to_model_file):
    """
    see: src/colmap/scene/reconstruction.cc
        void Reconstruction::ReadPoints3DBinary(const std::string& path)
        void Reconstruction::WritePoints3DBinary(const std::string& path)
    """
    points3D = {}
    with open(path_to_model_file, "rb") as fid:
        num_points = read_next_bytes(fid, 8, "Q")[0]
        for _ in range(num_points):
            binary_point_line_properties = read_next_bytes(
                fid, num_bytes=43, format_char_sequence="QdddBBBd"
            )
            point3D_id = binary_point_line_properties[0]
            xyz = np.array(binary_point_line_properties[1:4])
            rgb = np.array(binary_point_line_properties[4:7])
            error = np.array(binary_point_line_properties[7])
            track_length = read_next_bytes(fid, num_bytes=8, format_char_sequence="Q")[
                0
            ]
            track_elems = read_next_bytes(
                fid,
                num_bytes=8 * track_length,
                format_char_sequence="ii" * track_length,
            )
            image_ids = np.array(tuple(map(int, track_elems[0::2])))
            point2D_idxs = np.array(tuple(map(int, track_elems[1::2])))
            points3D[point3D_id] = Point3D(
                id=point3D_id,
                xyz=xyz,
                rgb=rgb,
                error=error,
                image_ids=image_ids,
                point2D_idxs=point2D_idxs,
            )
    return points3D


def write_points3D_text(points3D, path):
    """
    see: src/colmap/scene/reconstruction.cc
        void Reconstruction::ReadPoints3DText(const std::string& path)
        void Reconstruction::WritePoints3DText(const std::string& path)
    """
    if len(points3D) == 0:
        mean_track_length = 0
    else:
        mean_track_length = sum(
            (len(pt.image_ids) for _, pt in points3D.items())
        ) / len(points3D)
    HEADER = (
        "# 3D point list with one line of data per point:\n"
        + "#   POINT3D_ID, X, Y, Z, R, G, B, ERROR, TRACK[] as (IMAGE_ID, POINT2D_IDX)\n"
        + "# Number of points: {}, mean track length: {}\n".format(
            len(points3D), mean_track_length
        )
    )

    with open(path, "w") as fid:
        fid.write(HEADER)
        for _, pt in points3D.items():
            point_header = [pt.id, *pt.xyz, *pt.rgb, pt.error]
            fid.write(" ".join(map(str, point_header)) + " ")
            track_strings = []
            for image_id, point2D in zip(pt.image_ids, pt.point2D_idxs):
                track_strings.append(" ".join(map(str, [image_id, point2D])))
            fid.write(" ".join(track_strings) + "\n")


def write_points3D_binary(points3D, path_to_model_file):
    """
    see: src/colmap/scene/reconstruction.cc
        void Reconstruction::ReadPoints3DBinary(const std::string& path)
        void Reconstruction::WritePoints3DBinary(const std::string& path)
    """
    with open(path_to_model_file, "wb") as fid:
        write_next_bytes(fid, len(points3D), "Q")
        for _, pt in points3D.items():
            write_next_bytes(fid, pt.id, "Q")
            write_next_bytes(fid, pt.xyz.tolist(), "ddd")
            write_next_bytes(fid, pt.rgb.tolist(), "BBB")
            write_next_bytes(fid, pt.error, "d")
            track_length = pt.image_ids.shape[0]
            write_next_bytes(fid, track_length, "Q")
            for image_id, point2D_id in zip(pt.image_ids, pt.point2D_idxs):
                write_next_bytes(fid, [image_id, point2D_id], "ii")


def detect_model_format(path, ext):
    if (
        os.path.isfile(os.path.join(path, "cameras" + ext))
        and os.path.isfile(os.path.join(path, "images" + ext))
        and os.path.isfile(os.path.join(path, "points3D" + ext))
    ):
        print("Detected model format: '" + ext + "'")
        return True

    return False


def read_model(path, ext=""):
    # try to detect the extension automatically
    if ext == "":
        if detect_model_format(path, ".bin"):
            ext = ".bin"
        elif detect_model_format(path, ".txt"):
            ext = ".txt"
        else:
            print("Provide model format: '.bin' or '.txt'")
            return

    if ext == ".txt":
        cameras = read_cameras_text(os.path.join(path, "cameras" + ext))
        images = read_images_text(os.path.join(path, "images" + ext))
        points3D = read_points3D_text(os.path.join(path, "points3D") + ext)
    else:
        cameras = read_cameras_binary(os.path.join(path, "cameras" + ext))
        images = read_images_binary(os.path.join(path, "images" + ext))
        points3D = read_points3D_binary(os.path.join(path, "points3D") + ext)
    return cameras, images, points3D


def write_model(cameras, images, points3D, path, ext=".bin"):
    if ext == ".txt":
        write_cameras_text(cameras, os.path.join(path, "cameras" + ext))
        write_images_text(images, os.path.join(path, "images" + ext))
        write_points3D_text(points3D, os.path.join(path, "points3D") + ext)
    else:
        write_cameras_binary(cameras, os.path.join(path, "cameras" + ext))
        write_images_binary(images, os.path.join(path, "images" + ext))
        write_points3D_binary(points3D, os.path.join(path, "points3D") + ext)
    return cameras, images, points3D


def qvec2rotmat(qvec):
    return np.array(
        [
            [
                1 - 2 * qvec[2] ** 2 - 2 * qvec[3] ** 2,
                2 * qvec[1] * qvec[2] - 2 * qvec[0] * qvec[3],
                2 * qvec[3] * qvec[1] + 2 * qvec[0] * qvec[2],
            ],
            [
                2 * qvec[1] * qvec[2] + 2 * qvec[0] * qvec[3],
                1 - 2 * qvec[1] ** 2 - 2 * qvec[3] ** 2,
                2 * qvec[2] * qvec[3] - 2 * qvec[0] * qvec[1],
            ],
            [
                2 * qvec[3] * qvec[1] - 2 * qvec[0] * qvec[2],
                2 * qvec[2] * qvec[3] + 2 * qvec[0] * qvec[1],
                1 - 2 * qvec[1] ** 2 - 2 * qvec[2] ** 2,
            ],
        ]
    )


def rotmat2qvec(R):
    Rxx, Ryx, Rzx, Rxy, Ryy, Rzy, Rxz, Ryz, Rzz = R.flat
    K = (
        np.array(
            [
                [Rxx - Ryy - Rzz, 0, 0, 0],
                [Ryx + Rxy, Ryy - Rxx - Rzz, 0, 0],
                [Rzx + Rxz, Rzy + Ryz, Rzz - Rxx - Ryy, 0],
                [Ryz - Rzy, Rzx - Rxz, Rxy - Ryx, Rxx + Ryy + Rzz],
            ]
        )
        / 3.0
    )
    eigvals, eigvecs = np.linalg.eigh(K)
    qvec = eigvecs[[3, 0, 1, 2], np.argmax(eigvals)]
    if qvec[0] < 0:
        qvec *= -1
    return qvec


def standard_qt_to_pose(qvec, tvec):
    # qvec: (x, y, z, w)
    # tvec: (3,)
    
    # convert to rotation matrix
    r = R.from_quat(qvec)
    R_mat = r.as_matrix()
    
    # build pose matrix Tcw
    Twc = np.eye(4)
    Twc[:3, :3] = R_mat
    Twc[:3, 3] = tvec
    return Twc

def pose_to_colmap_qt(Tcw):
    # extract rotation and translation
    R_mat = Tcw[:3, :3]
    tvec = Tcw[:3, 3]

    # convert rotation matrix back to quaternion
    r = R.from_matrix(R_mat)
    quat_xyzw = r.as_quat()  # (x, y, z, w)
    qvec = np.roll(quat_xyzw, 1)  # to COLMAP style (w, x, y, z)
    return qvec, tvec

def invert_pose(T):
    R_mat = T[:3, :3]
    t = T[:3, 3]
    
    T_inv = np.eye(4)
    T_inv[:3, :3] = R_mat.T
    T_inv[:3, 3] = -R_mat.T @ t

    return T_inv


def write_colmap_pose_file(poses, angles, output_path):
    with open(output_path, "w") as f:
        f.write("# id qw qx qy qz tx ty tz\n")
        for (p_id, tvec), (a_id, qvec) in zip(poses.items(), angles.items()):
            assert p_id == a_id

            # qvec is (x, y, z, w) format by default
            # COLMAP style is (w, x, y, z)
            Twc = standard_qt_to_pose(qvec, tvec)
            Tcw = invert_pose(Twc) # COLMAP style
            qvec, tvec = pose_to_colmap_qt(Tcw)
            # write
            line = f"{str(p_id)} {' '.join(map(str, qvec))} {' '.join(map(str, tvec))}\n"
            f.write(line)


def load_waypoints(waypoints_file: str):
    """load waypoint coordinates from text file and return dict {id: coords}"""
    if not os.path.exists(waypoints_file):
        raise FileNotFoundError(f"waypoints file not found: {waypoints_file}")

    # read file, skip comments and empty lines
    with open(waypoints_file, 'r') as f:
        lines = [line.strip() for line in f if not line.startswith('#') and line.strip()]

    waypoints_dict = {}
    for line in lines:
        parts = line.split()
        if len(parts) != 4:
            continue  # skip malformed lines
        
        waypoint_id = parts[0]
        try:
            # convert x,y,z to float32 numpy array
            coords = np.array([float(x) for x in parts[1:]], dtype=np.float32)
        except ValueError:
            continue  # skip lines with invalid numbers
        
        waypoints_dict[waypoint_id] = coords

    if not waypoints_dict:
        raise ValueError("no valid waypoints found in the file")
    
    # log number of waypoints loaded
    logging.info(f"loaded {len(waypoints_dict)} waypoints from {waypoints_file}")
    return waypoints_dict


def load_sfm_model(sfm_dir: str):
    """load colmap sfm reconstruction model"""
    if not os.path.isdir(sfm_dir):
        raise FileNotFoundError(f"sfm directory not found: {sfm_dir}")

    # try binary format first, fallback to text
    try:
        cameras, images, points3D = read_model(sfm_dir, ext=".bin")
        logging.info(
            f"loaded sfm model (binary): {len(images)} images, {len(points3D)} points"
        )
    except Exception:
        try:
            cameras, images, points3D = read_model(sfm_dir, ext=".txt")
            logging.info(
                f"loaded sfm model (text): {len(images)} images, {len(points3D)} points"
            )
        except Exception as e:
            raise RuntimeError(f"failed to load sfm model from {sfm_dir}: {e}")

    if not images:
        raise ValueError("no images found in the sfm model")

    return cameras, images, points3D


